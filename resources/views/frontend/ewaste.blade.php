                    	<h3>E-Waste Management and Handling Rules</h3>
                        	<p>E-waste is a popular, informal name for electronic products nearing the end of their useful life. E-wastes are considered dangerous, as certain components of some electronic products contain materials that are hazardous, depending on their condition and density. The hazardous content of these materials pose a threat to human health and environment. Discarded  computers, refrigerators, washing machines, televisions, VCRs, stereos, copiers, fax machines, electric lamps, cell phones, audio equipment, other Electronic Itemsand batteries if improperly disposed can leach lead and other substances into soil and groundwater. Many of these products can be reused,  refurbished, or recycled in an environmentally sound manner so that they are less harmful to the ecosystem.</p>
