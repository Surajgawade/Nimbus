                        <div class="pro-in">
                    	<h3>Benefits of E-Waste Recycling</h3>
							<div class="waste-img">
							<img src="{{asset('public/frontend/images/benefits.jpg')}}">
							</div>
                        	<p><strong>Electronics Recycling Conserves Natural Resources</strong></p>
							<p>There are many materials that can be recovered from old electronics. These  materials can be used to make new products, thus reducing the need to mine for new raw materials. For instance, various metals can be recovered from computer circuit boards and other electronics, and the plastics and glass found in computer monitors and televisions can be recycled.</p>
                        	<p><strong>Electronics Recycling Supports the Community</strong></p>
							<p>Donating your old electronics plays an important role in the provision of refurbished products such as computers and mobile phones, which can be of great help to low-income families, schools, and not-for-profit organizations. It also helps individuals gain access to technology that they could not have otherwise forded.</p>
                        	<p><strong>Electronics Recycling Creates Employment Locally</strong></p>
							<p>Considering that around 90  percent of  electronic  equipment  is  recyclable,  electronics  recycling can play a significant role in creating  employment.  This  is  because   new  firms  dealing  with  electronics recycling will form and existing firms will look to  employ  more  people  to  recover recyclable  materials.  This  can  be  triggered  by  the  increase  in  the  demand  for  electronics recycling.</p>
							<p><strong>Electronics Recycling Helps Protect Public Health and the Environment</strong></p>
							<p>Many electronics have toxic or hazardous materials  such  as  mercury  and  lead,  which  can  be harmful to the environment if  disposed  of  in  trashcans.  Reusing  and  recycling  electronics  safely helps  in  keeping  the  hazardous  materials  from  harming  humans   or   the   environment.   For example, televisions and computer monitors are hazardous since they have lead in them.  Printed circuit boards contain harmful materials such as cadmium, lead, mercury and chromium. Also, batteries in computers and other electronics may contain hazardous materials such as cadmium, mercury and lead. Instead of keeping old electronics in the house or dumping them in landfills, recycling or reusing them is an appropriate option that should be supported by individuals and organizations.  Considering the benefits of electronics recycling,   it   is   very important that people in various parts around the world embrace this concept.</p>
							<p><strong>Creates Jobs</strong></p>
							<p>E-waste recycling creates new jobs for professional recyclers and creates a second market for the recycled materials.</p>
                           
